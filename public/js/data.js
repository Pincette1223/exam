function get_quers(){
    // query를 가져오는 함수
    urls = location.search.split("&")
    quers = {}


    for(var i = 0; i < urls.length; i++){
        url_data = urls[i].split("=") 
        if (i == 0){
            quers[url_data[0].slice(1)] = url_data[1]
        }   else{
            quers[url_data[0]] = url_data[1]
        }
        // console.log(urls[i])
    }
    return quers
}

id = Number(get_quers()["id"])
// alert(id)
if ((window.location.href).indexOf("index.html") === -1){
    // index.html이 아닌지 판단
    if(isNaN(id) || id < 0 || id > 3){
        // id가 없거나, id가 지정된 범위 밖일때 메인페이지로 이동한다.
        location.href = "/"
    }
}

